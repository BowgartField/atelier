import assert from 'node:assert/strict'
import { readFileSync, writeFileSync } from 'node:fs'

const check = process.argv.includes('--check')
const dispatchPath = 'src-tauri/src/http_server/dispatch.rs'
const capabilitiesPath = 'crates/jean-core/src/capabilities.rs'
const outputPath = 'crates/jean-core/src/capabilities_generated.rs'

const commandArms = source =>
  [...source.matchAll(/^ {8}"([a-zA-Z0-9_]+)" =>/gm)].map(
    match => match[1]
  )

const desktopCommands = commandArms(readFileSync(dispatchPath, 'utf8'))
const capabilitiesSource = readFileSync(capabilitiesPath, 'utf8')
const supportedBlock = capabilitiesSource.match(
  /pub const HEADLESS_CAPABILITIES:[\s\S]*?= core_capabilities!\[([\s\S]*?)\n\];/
)?.[1]
assert.ok(supportedBlock, 'Could not find HEADLESS_CAPABILITIES')
const supported = new Set(
  [...supportedBlock.matchAll(/"([a-zA-Z0-9_]+)"/g)].map(match => match[1])
)

const explicitDesktopOnly = new Set([
  'clear_window_vibrancy',
  'close_auth_window',
  'close_main_window',
  'is_window_maximized',
  'open_auth_window',
  'open_main_window',
  'set_window_shadow',
  'set_window_vibrancy',
  'start_window_dragging',
])

const adapterOnlyExtras = [
  'open_file_dialog',
  'open_folder_dialog',
  'open_file_in_default_app',
]

const unavailable = [...new Set([...desktopCommands, ...adapterOnlyExtras])]
  .filter(command => !supported.has(command))
  .sort()

const entries = unavailable
  .map(command => {
    const classification = explicitDesktopOnly.has(command)
      ? 'DesktopOnly'
      : 'AdapterBacked'
    return `    CommandCapability {\n        command: "${command}",\n        class: CapabilityClass::${classification},\n        available: false,\n    },`
  })
  .join('\n')

const generated = `// @generated by scripts/generate-headless-capabilities.mjs — do not edit manually.\nuse crate::capabilities::{CapabilityClass, CommandCapability};\n\npub const UNAVAILABLE_CAPABILITIES: &[CommandCapability] = &[\n${entries}\n];\n`

if (check) {
  assert.equal(
    readFileSync(outputPath, 'utf8'),
    generated,
    'Headless capability inventory is stale; run node scripts/generate-headless-capabilities.mjs'
  )
} else {
  writeFileSync(outputPath, generated)
}
